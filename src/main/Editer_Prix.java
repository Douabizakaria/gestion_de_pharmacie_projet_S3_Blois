    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
int row = jTable1.getSelectedRow();
String t = jTable1.getModel().getValueAt(row,1).toString();
 String sql = "select CODE_BARRES,PRIX_VENTE from medicament where CODE_BARRES='"+t+"' ";
try{
    pre=con.prepareStatement(sql);
    res=pre.executeQuery();
    if(res.next()){
        String Price = res.getString("PRIX_VENTE"); 
        Ancien_prix.setText(Price);
        
        String Barcode = res.getString("CODE_BARRES"); 
        code_barres.setText(Barcode);
    }
}catch(Exception e){
       JOptionPane.showMessageDialog(null,e.getMessage(),"Error",2); 
}
    }//GEN-LAST:event_jTable1MouseClicked

